#include<stdio.h>
void main()
{
        int a , b , sum;
        printf("\nEnter the first number\n"); // 1st number
        scanf("%d",&a); // a will store the first number
        printf("\nEnter the second number\n"); // 2nd number
        scanf("%d",&b); // b will store the 2nd number
        sum = a + b; // sum will the addition of the value present in a and b
        printf("\nThe sum of %d and %d is %d\n",a,b,sum); // print the value of sum

}
